// Replace the first appearance of C++ from a string with JavaScript.
function replaceCppWithJavaScript(str) {
    // Replaces only the first instance of 'C++' with 'JavaScript'
    return str.replace('C++', 'JavaScript');
}

console.log(replaceCppWithJavaScript('Learning C++ is pretty easy, ngl!'))
// Expected output: Learning JavaScript is pretty easy, ngl!
